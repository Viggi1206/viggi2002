# Calculate the circumference of a circle with diameter d.

# create the function
def calculate_circle_circumference(diameter):
#check the circle circumference formula 
    circle_circumference = 3.14 * diameter
#create the return statement 
    return circle_circumference
#answer are required 
d = int(input("Enter the diameter : "))
# print the function
print(calculate_circle_circumference(diameter = d),"is a circle circumference value.") 
